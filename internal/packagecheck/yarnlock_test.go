func TestParseYarnLock_BerryParsed(t *testing.T) {
	// A yarn Berry (v2+) lockfile carries __metadata: and is parsed via
	// the resolution: field. The alias descriptor resolves to the REAL
	// package (node-ipc), the workspace entry is skipped, and a scoped
	// package is handled.
	refs, err := ParseYarnLock(writeYarn(t, `# This file is generated by running "yarn install"
__metadata:
  version: 8
  cacheKey: 10c0

"lodash@npm:^4.17.21":
  version: 4.17.21
  resolution: "lodash@npm:4.17.21"
  languageName: node
  linkType: hard

"@babel/core@npm:^7.0.0":
  version: 7.23.0
  resolution: "@babel/core@npm:7.23.0"
  languageName: node
  linkType: hard

"safe-ipc@npm:node-ipc@9.2.3":
  version: 9.2.3
  resolution: "node-ipc@npm:9.2.3"
  languageName: node
  linkType: hard

"app@workspace:.":
  version: 0.0.0-use.local
  resolution: "app@workspace:."
  languageName: unknown
  linkType: soft
`))
	require.NoError(t, err)
	require.ElementsMatch(t,
		[]string{"lodash@4.17.21", "@babel/core@7.23.0", "node-ipc@9.2.3"},
		refSet(refs),
		"alias must resolve to node-ipc; workspace entry must be skipped")
	for _, r := range refs {
		require.Equal(t, intel.EcosystemNPM, r.Ecosystem)
		require.Equal(t, "yarn.lock", r.Source)
	}
}

func TestParseYarnLock_V1WithMetadataCommentStillParsedAsV1(t *testing.T) {
	// A classic v1 lockfile containing `__metadata:` only in a comment
	// must NOT be routed to the Berry parser (which would read zero
	// packages and hide every v1 dependency).
	refs, err := ParseYarnLock(writeYarn(t, `# __metadata: this is just a comment, not a Berry header
lodash@^4.17.21:
  version "4.17.21"
  resolved "https://registry.yarnpkg.com/lodash/-/lodash-4.17.21.tgz#abc"
`))
	require.NoError(t, err)
	require.Equal(t, []string{"lodash@4.17.21"}, refSet(refs), "v1 lockfile must parse as v1 despite a __metadata: comment")
}

func TestParseYarnLock_BerryInternalBlankLine(t *testing.T) {
	// A hand-edited block with a blank line before resolution: must still
	// be read (a blank line does not end the block; only the next header
	// does).
	refs, err := ParseYarnLock(writeYarn(t, `__metadata:
  version: 8

"lodash@npm:4.17.21":
  version: 4.17.21

  resolution: "lodash@npm:4.17.21"
  languageName: node

"left-pad@npm:1.3.0":
  version: 1.3.0
  resolution: "left-pad@npm:1.3.0"
`))
	require.NoError(t, err)
	require.ElementsMatch(t, []string{"lodash@4.17.21", "left-pad@1.3.0"}, refSet(refs),
		"an internal blank line must not hide a package")
}

func TestParseYarnLock_BerryCRLF(t *testing.T) {
	// A Windows/CRLF checkout must parse identically to LF.
	lf := "__metadata:\n  version: 8\n\n\"lodash@npm:4.17.21\":\n  version: 4.17.21\n  resolution: \"lodash@npm:4.17.21\"\n"
	crlf := strings.ReplaceAll(lf, "\n", "\r\n")
	refs, err := ParseYarnLock(writeYarn(t, crlf))
	require.NoError(t, err)
	require.Equal(t, []string{"lodash@4.17.21"}, refSet(refs), "CRLF Berry lockfile must read its packages")
}

func TestParseYarnLock_BerryAliasRetainingResolution(t *testing.T) {
	// Defensive: if a Berry resolution ever retains the alias ident
	// instead of normalizing to the real package, the real package is
	// still extracted, not the alias -- in either the single-@npm:
	// embedded form (alias@npm:real@version) or a double-@npm: form.
	for _, resolution := range []string{
		"safe-ipc@npm:node-ipc@9.2.3",     // alias retained, real embedded as name@version
		"safe-ipc@npm:node-ipc@npm:9.2.3", // alias retained, double @npm:
	} {
		refs, err := ParseYarnLock(writeYarn(t, `__metadata:
  version: 8

"safe-ipc@npm:node-ipc@9.2.3":
  version: 9.2.3
  resolution: "`+resolution+`"
`))
		require.NoError(t, err)
		require.Equal(t, []string{"node-ipc@9.2.3"}, refSet(refs),
			"must resolve to the real package, not the alias, for resolution %q", resolution)
	}
}

func TestParseYarnLock_BerryNestedFieldsNotMiscaptured(t *testing.T) {
	// A nested dependency named "version"/"resolution" (4-space indent)
	// must NOT be mistaken for the block's own resolution. `good` must
	// resolve to 1.0.0 (from its own 2-space resolution), never 9.9.9 or
	// 8.8.8 from the nested deps.
	refs, err := ParseYarnLock(writeYarn(t, `__metadata:
  version: 8

"good@npm:1.0.0":
  version: 1.0.0
  resolution: "good@npm:1.0.0"
  dependencies:
    version: "npm:9.9.9"
    resolution: "npm:8.8.8"
`))
	require.NoError(t, err)
	require.Equal(t, []string{"good@1.0.0"}, refSet(refs),
		"nested version/resolution deps must not be captured as the block's own fields")
}

