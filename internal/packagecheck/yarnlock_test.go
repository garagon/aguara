func TestYarnDescriptorParse(t *testing.T) {
	cases := []struct {
		desc, name, rng string
		ok              bool
	}{
		{"lodash@^4.17.0", "lodash", "^4.17.0", true},
		{`"@scope/pkg@^1.2.0"`, "@scope/pkg", "^1.2.0", true},
		{"alias@npm:real@^1.0.0", "alias", "npm:real@^1.0.0", true},
		{"@scope/pkg@npm:other@^1.0.0", "@scope/pkg", "npm:other@^1.0.0", true},
		{"noatsign", "", "", false},
		{"", "", "", false},
		{"foo@", "", "", false},
		{`"@scope/pkg@"`, "", "", false},
	}
	for _, c := range cases {
		n, r, ok := yarnDescriptorParse(c.desc)
		require.Equalf(t, c.ok, ok, "ok for %q", c.desc)
		if c.ok {
			require.Equalf(t, c.name, n, "name for %q", c.desc)
			require.Equalf(t, c.rng, r, "range for %q", c.desc)
		}
	}
}

func TestIsExactYarnVersion(t *testing.T) {
	require.True(t, isExactYarnVersion("4.17.21"))
	require.True(t, isExactYarnVersion("1.0.0-beta.1+build"))
	require.False(t, isExactYarnVersion(""))
	require.False(t, isExactYarnVersion("^1.0.0"))
	require.False(t, isExactYarnVersion(">=1.0.0 <2.0.0"))
	require.False(t, isExactYarnVersion("npm:real@1.0.0"))
}

func TestRunner_YarnLockHit(t *testing.T) {
	snap := intel.Snapshot{
		SchemaVersion: intel.CurrentSchemaVersion,
		GeneratedAt:   time.Date(2026, 1, 1, 0, 0, 0, 0, time.UTC),
		Records: []intel.Record{{
			ID:        "MAL-YARN",
			Ecosystem: intel.EcosystemNPM,
			Name:      "evil-pkg",
			Kind:      intel.KindMalicious,
			Versions:  []string{"1.2.3"},
		}},
	}
	runner := &Runner{Matcher: intel.NewMatcher(snap)}
	target := writeYarn(t, yarnHeader+`evil-pkg@^1.0.0:
  version "1.2.3"
  resolved "https://registry.yarnpkg.com/evil-pkg/-/evil-pkg-1.2.3.tgz#a"

safe@^9.0.0:
  version "9.9.9"
  resolved "https://registry.yarnpkg.com/safe/-/safe-9.9.9.tgz#b"
`)
	res, err := runner.Run([]Target{target})
	require.NoError(t, err)
	require.Len(t, res.Ecosystems, 1)
	require.Equal(t, "yarn.lock", res.Ecosystems[0].Source)
	require.Equal(t, 2, res.Ecosystems[0].PackagesRead)
	require.Equal(t, 1, res.Ecosystems[0].FindingsCount)
	require.Len(t, res.Hits, 1)
	require.Equal(t, "evil-pkg", res.Hits[0].Ref.Name)
	require.Equal(t, "MAL-YARN", res.Hits[0].Record.ID)
}

func TestDiscover_PicksYarnLock(t *testing.T) {
	dir := t.TempDir()
	require.NoError(t, os.WriteFile(filepath.Join(dir, "yarn.lock"), []byte("# yarn lockfile v1\n"), 0o600))
	targets, err := Discover(dir, []string{intel.EcosystemNPM})
	require.NoError(t, err)
	require.Len(t, targets, 1)
	require.Equal(t, "yarn.lock", targets[0].Source)
	require.Equal(t, intel.EcosystemNPM, targets[0].Ecosystem)
}
