// writeYarn writes content to a yarn.lock in a fresh temp dir and
// returns a Target pointing at it.
func writeYarn(t *testing.T, content string) Target {
	t.Helper()
	dir := t.TempDir()
	path := filepath.Join(dir, "yarn.lock")
	require.NoError(t, os.WriteFile(path, []byte(content), 0o600))
	return Target{Ecosystem: intel.EcosystemNPM, Path: path, Source: "yarn.lock"}
}

const yarnHeader = "# THIS IS AN AUTOGENERATED FILE.\n# yarn lockfile v1\n\n\n"

func TestParseYarnLock_BasicAndScoped(t *testing.T) {
	refs, err := ParseYarnLock(writeYarn(t, yarnHeader+`lodash@^4.17.0:
  version "4.17.21"
  resolved "https://registry.yarnpkg.com/lodash/-/lodash-4.17.21.tgz#abc"
  integrity sha512-FAKE==

"@scope/pkg@^1.2.0":
  version "1.2.3"
  resolved "https://registry.yarnpkg.com/@scope/pkg/-/pkg-1.2.3.tgz#def"
`))
	require.NoError(t, err)
	require.ElementsMatch(t, []string{"lodash@4.17.21", "@scope/pkg@1.2.3"}, refSet(refs))
	for _, r := range refs {
		require.Equal(t, intel.EcosystemNPM, r.Ecosystem)
		require.Equal(t, "yarn.lock", r.Source)
	}
}

